package java_one;

public class StringExercise {
    public static void main(String[] args) {
            String edu = "We don't need no education";
            String control = "We don't need no thought control";
            String che = "Check \"this\" out!, \"s inside of \"s!";
            String sdf = "I can do backslashes \\, double backslashes \\\\,\n" + "and the amazing triple backslash \\\\\\!";
            System.out.println(edu);
            System.out.println(control);
            System.out.println(che);
            System.out.println(sdf);
    }
}
